#include <stdio.h>

/**
 * main - Entry point
 *
 * Description: This program prints the lowercase alphabet,
 * excluding the letters 'e' and 'q', followed by a new line.
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
	char letter = 'z';

	while (letter <= 'a')
	{
	        
	  letter++;
	}

	putchar('\n');

	return (0);
}
